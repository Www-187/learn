#约束
#1.约束的分类

#1.1根据约束的字段个数
#单列约束：id
#多列约束：id+name


#1.2根据约束的范围
#列级约束：将此约束声明在对应字段的后面
#表级约束：所有字段声明完以后，在所有字段后面声明的约束

#1.3根据约束的作用
#1.3.1 NOT NULL（非空约束）
#1.3.2 UIQUE（唯一性约束）
#1.3.3 PRIMARY KEY （主键约束）
#1.3.4 FOREIGN KEY （外键约束）
#1.3.5 CHECK（检查约束）
#1.3.6 DEFAULT（默认值约束）


#1.4如何添加约束
#可以在创建表时添加约束
#在修改表时添加和删除约束









